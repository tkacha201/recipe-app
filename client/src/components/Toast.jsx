import { toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

// Success notifications
export const showSuccess = (message) => {
  toast.success(message, {
    position: "top-right",
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
  });
};

